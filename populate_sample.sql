-- Script para crear tablas SAMPLE básicas
-- Ejecutar como db2inst1 en la base SAMPLE

-- Crear tabla DEPARTMENT
CREATE TABLE DEPARTMENT (
    DEPTNO CHAR(3) NOT NULL,
    DEPTNAME VARCHAR(36) NOT NULL,
    MGRNO CHAR(6),
    ADMRDEPT CHAR(3) NOT NULL,
    LOCATION CHAR(16),
    PRIMARY KEY (DEPTNO)
);

-- Crear tabla EMPLOYEE
CREATE TABLE EMPLOYEE (
    EMPNO CHAR(6) NOT NULL,
    FIRSTNME VARCHAR(12) NOT NULL,
    MIDINIT CHAR(1),
    LASTNAME VARCHAR(15) NOT NULL,
    WORKDEPT CHAR(3),
    PHONENO CHAR(4),
    HIREDATE DATE,
    JOB CHAR(8),
    EDLEVEL SMALLINT,
    SEX CHAR(1),
    BIRTHDATE DATE,
    SALARY DECIMAL(9,2),
    BONUS DECIMAL(9,2),
    COMM DECIMAL(9,2),
    PRIMARY KEY (EMPNO)
);

-- Crear tabla PROJECT
CREATE TABLE PROJECT (
    PROJNO CHAR(6) NOT NULL,
    PROJNAME VARCHAR(24) NOT NULL,
    DEPTNO CHAR(3),
    RESPEMP CHAR(6),
    PRSTAFF DECIMAL(5,2),
    PRSTDATE DATE,
    PRENDATE DATE,
    MAJPROJ CHAR(6),
    PRIMARY KEY (PROJNO)
);

